#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n > 9 || n < -9)
	{
		ft_putnbr_fd((n - (n % 10)) / 10, fd);
		n -= (n - (n % 10));
		if (n < 0)
			n *= -1;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd((n * -1) + '0', fd);
	}
	else
		ft_putchar_fd((n) + '0', fd);
}
